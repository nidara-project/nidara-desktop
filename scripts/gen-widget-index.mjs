#!/usr/bin/env node
/**
 * gen-widget-index.mjs
 * Auto-registration for shell widgets: scans ui/shell/widgets/ and
 * generates widgets.gen.ts (static imports + ALL_WIDGETS array) so adding a
 * widget = dropping ONE file in that directory — no manual registry edit.
 *
 * A file there must either be an AtomicWidget module
 * (`const w: AtomicWidget = {...}` + `export default w`) or be listed in
 * EXCLUDE — anything else is a hard error, so helpers can't sneak in and
 * malformed widgets can't be skipped silently. New helpers belong in
 * ui/shell/common/, not in widgets/.
 *
 * Usage:  node scripts/gen-widget-index.mjs
 * Runs automatically via npm pre-hooks (build/dev/run) and, fail-soft, in
 * bin/nidara-ui's dev branch. CI job "widgets-gen" fails if the
 * committed widgets.gen.ts is stale.
 */

import { readFileSync, writeFileSync, readdirSync, existsSync } from "fs"
import { join } from "path"

const ROOT = new URL("..", import.meta.url).pathname
const WIDGETS_DIR = join(ROOT, "ui/shell/widgets")
const OUT_FILE = join(WIDGETS_DIR, "widgets.gen.ts")

// Non-widget files allowed to live in widgets/. bar-helpers.ts is
// grandfathered; do NOT add new helpers here — put them in common/.
const EXCLUDE = new Set(["index.ts", "widgets.gen.ts", "bar-helpers.ts"])

const files = readdirSync(WIDGETS_DIR)
    .filter(f => /\.(ts|tsx)$/.test(f) && !EXCLUDE.has(f))
    .sort() // byte order — deterministic across machines/locales

const errors = []
const widgets = [] // { file, importPath, ident, id }
const seenIds = new Map() // id → file

for (const file of files) {
    const src = readFileSync(join(WIDGETS_DIR, file), "utf8")
    const declMatch = src.match(/:\s*AtomicWidget\s*=\s*\{/)
    const hasDefault = /export\s+default\s/.test(src)

    if (!declMatch || !hasDefault) {
        errors.push(
            `${file}: not an AtomicWidget module (needs \`const w: AtomicWidget = {...}\` ` +
            `+ \`export default w\`). widgets/ is a widgets-only directory — move helpers ` +
            `to ui/shell/common/, or add the file to EXCLUDE in scripts/gen-widget-index.mjs.`
        )
        continue
    }

    // First `id: "..."` AFTER the AtomicWidget literal opens — a global match
    // would hit unrelated literals (e.g. screenshot.ts mode objects).
    const after = src.slice(declMatch.index)
    const idMatch = after.match(/\bid:\s*"([^"]+)"/)
    if (!idMatch) {
        errors.push(`${file}: AtomicWidget literal has no \`id: "..."\` field.`)
        continue
    }
    const id = idMatch[1]
    if (seenIds.has(id)) {
        errors.push(`duplicate widget id "${id}" in ${file} (already used by ${seenIds.get(id)}).`)
        continue
    }
    seenIds.set(id, file)

    const importPath = "./" + file.replace(/\.(ts|tsx)$/, "")
    // cpu-memory → cpuMemory; night-light → nightLight
    const ident = file.replace(/\.(ts|tsx)$/, "").replace(/-(\w)/g, (_, c) => c.toUpperCase())
    widgets.push({ file, importPath, ident, id })
}

if (errors.length > 0) {
    console.error("gen-widget-index: ERRORS\n" + errors.map(e => "  ✗ " + e).join("\n"))
    console.error("Fix the above, then re-run: node scripts/gen-widget-index.mjs")
    process.exit(1)
}

const header = `// ─────────────────────────────────────────────────────────────────────────────
// AUTO-GENERATED by scripts/gen-widget-index.mjs — DO NOT EDIT.
// One file in widgets/ = one registered widget. Regenerate with:
//   node scripts/gen-widget-index.mjs
// (also runs on npm run build/dev and before \`ags run\` in the dev launcher;
// the CI job "widgets-gen" fails if this committed file is stale.)
// ─────────────────────────────────────────────────────────────────────────────
`

const out = header
    + `import { AtomicWidget } from "../surfaces/control-center/Types"\n`
    + widgets.map(w => `import ${w.ident} from "${w.importPath}"`).join("\n")
    + `\n\nexport const ALL_WIDGETS: AtomicWidget[] = [\n`
    + widgets.map(w => `    ${w.ident},`).join("\n")
    + `\n]\n`

const prev = existsSync(OUT_FILE) ? readFileSync(OUT_FILE, "utf8") : null
if (prev === out) {
    console.log(`gen-widget-index: ${widgets.length} widgets → widgets.gen.ts (unchanged)`)
} else {
    writeFileSync(OUT_FILE, out)
    console.log(`gen-widget-index: ${widgets.length} widgets → widgets.gen.ts (updated)`)
}
